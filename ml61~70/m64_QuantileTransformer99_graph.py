import matplotlib.pyplot as plt
import numpy as np
from sklearn.datasets import make_blobs
from sklearn.preprocessing import QuantileTransformer, StandardScaler, PowerTransformer

x, y = make_blobs(random_state=337,
                  n_samples=50, 
                  centers=2, # 클러스터 갯수 / y의 라벨
                  cluster_std=1, # 클러스터의 표준편차
                  )

fig, ax = plt.subplots(2, 2, figsize=(12, 8))

ax[0, 0].scatter(x[:, 0], x[:, 1],
            c=y,
            edgecolors='black',
            )
ax[0,0].set_title('ORIGIN')

scaler = QuantileTransformer(n_quantiles=50)
ax[0, 1].scatter(x[:, 0], x[:, 1],
            c=y,
            edgecolors='black',
            )
ax[0, 1].set_title('QuantileTransformer')


scaler = PowerTransformer()
x_trans = scaler.fit_transform(x)
ax[1, 0].scatter(x[:, 0], x[:, 1],
            c=y,
            edgecolors='black',
            )
ax[1, 0].set_title('PowerTransformer')


scaler = StandardScaler()
x_trans = scaler.fit_transform(x)
ax[1, 1].scatter(x[:, 0], x[:, 1],
            c=y,
            edgecolors='black',
            )
ax[1, 1].set_title('StandardScaler')


plt.show()




